public class Operadores2 {
	
	public static void main (String[] args) {
		
		int numero = 5;
		
		numero = - numero; //transformando número positivo em negativo
		
		System.out.println(numero);
		
		numero = numero * -1;  //transformando número negativo em positivo
				
		System.out.println(numero);
		
	}
	
}
