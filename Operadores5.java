public class Operadores5 {
	
	public static void main(String[] args) {
		
		int a,b;
		a = 5;
		b = 9;
		
		boolean resultado = a == b;
		System.out.println("A é igual a B?   " + resultado);
		
		resultado = a != b;
		System.out.println("A é diferente de B?  " + resultado);
		
		resultado = a > b;
		System.out.println("A é maior que B?  " + resultado);
		
		resultado = a < b;
		System.out.println("A é menor que B?  " + resultado);
	}
}